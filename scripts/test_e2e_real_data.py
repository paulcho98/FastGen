# TEST 1: ODE KD with ODE trajectories generated by OUR script
# ============================================================
def test_ode_kd():
    print_header("TEST 1: ODE KD — 1.3B causal student on ODE trajectories (from our generation script)")
    torch.cuda.reset_peak_memory_stats(DEVICE)

    from fastgen.networks.OmniAvatar.network_causal import CausalOmniAvatarWan

    # Load real sample WITH our generated ODE path (ode_path.pt)
    data = load_real_sample(need_path=True)
    assert "path" in data, "No ode_path.pt found! Run generate_omniavatar_ode_pairs.py first."
    print(f"  path shape: {data['path'].shape}, dtype: {data['path'].dtype}")

    condition = build_condition(data)

    # Build 1.3B causal student
    print("\n  Building 1.3B causal student for KD training...")
    student = CausalOmniAvatarWan(
        model_size="1.3B",
        in_dim=65,
        mode="v2v",
        use_audio=True,
        audio_hidden_size=32,
        chunk_size=3,
        total_num_frames=21,
        base_model_paths=BASE_1_3B,
        omniavatar_ckpt_path=None,
        net_pred_type="flow",
        schedule_type="rf",
    )
    student = student.to(device=DEVICE, dtype=DTYPE)
    student.train()
    print(f"  Student loaded: {gpu_mem():.1f} GB")

    # Simulate CausalKDModel.single_train_step logic
    ode_path = data["path"]  # [1, 4, 16, 21, 64, 64]
    real_data = data["real"]
    B, num_frames = ode_path.shape[0], ode_path.shape[3]
    chunk_size = student.chunk_size

    # Sample inhomogeneous timesteps
    t_inhom, ids = student.noise_scheduler.sample_t_inhom(
        B, num_frames, chunk_size,
        sample_steps=4,
        t_list=[0.999, 0.937, 0.833, 0.624, 0.0],
        device=DEVICE,
        dtype=ode_path.dtype,
    )
    print(f"  t_inhom: {t_inhom.shape}, sample: {t_inhom[0,:9].tolist()}")

    # Gather from path
    expand_shape = [ids.shape[0], 1, 1, ids.shape[1]] + [1] * max(0, ode_path.ndim - 4)
    ids_exp = ids.view(expand_shape).expand(-1, -1, *ode_path.shape[2:])
    denoise_path_all = torch.cat([ode_path, real_data.unsqueeze(1)], dim=1)
    noisy_data = torch.gather(denoise_path_all, 1, ids_exp).squeeze(1)
    print(f"  noisy_data: {noisy_data.shape}")

    # Student forward (full-sequence with inhomogeneous t)
    gen_data = student(noisy_data, t_inhom, condition=condition, fwd_pred_type="x0", is_ar=False)
    print(f"  gen_data: {gen_data.shape}")

    # L2 loss + backward
    loss = 0.5 * F.mse_loss(gen_data, real_data, reduction="mean")
    loss.backward()

    grad_count = sum(1 for p in student.parameters() if p.grad is not None)
    print(f"\n  Loss: {loss.item():.6f}")
    print(f"  Params with gradients: {grad_count}")
    print(f"  Peak memory: {gpu_peak():.1f} GB")
    print(f"  TEST 1 PASSED!")

    del student, gen_data, noisy_data, data
    gc.collect()
    torch.cuda.empty_cache()
    return True


