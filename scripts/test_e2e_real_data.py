# TEST 2: Diffusion Forcing on real video data
# ============================================================
def test_diffusion_forcing():
    print_header("TEST 2: Diffusion Forcing — 1.3B causal student on real video data")
    torch.cuda.reset_peak_memory_stats(DEVICE)

    from fastgen.networks.OmniAvatar.network_causal import CausalOmniAvatarWan

    # Load real sample (no ODE path needed)
    data = load_real_sample(need_path=False)

    # Build student
    print("\n  Building 1.3B causal student...")
    student = CausalOmniAvatarWan(
        model_size="1.3B",
        in_dim=65,
        mode="v2v",
        use_audio=True,
        audio_hidden_size=32,
        chunk_size=3,
        total_num_frames=21,
        base_model_paths=BASE_1_3B,
        omniavatar_ckpt_path=None,
        net_pred_type="flow",
        schedule_type="rf",
    )
    student = student.to(device=DEVICE, dtype=DTYPE)
    student.train()
    print(f"  Student loaded: {gpu_mem():.1f} GB")

    condition = build_condition(data)
    real_data = data["real"]
    B, num_frames = real_data.shape[0], real_data.shape[2]
    chunk_size = student.chunk_size

    # Sample inhomogeneous timesteps (diffusion forcing)
    t_inhom, _ = student.noise_scheduler.sample_t_inhom(
        B, num_frames, chunk_size,
        sample_steps=4,
        t_list=[0.999, 0.937, 0.833, 0.624, 0.0],
        device=DEVICE,
        dtype=real_data.dtype,
    )
    print(f"  t_inhom: {t_inhom.shape}")

    # Add noise to real data (NOT ODE path)
    eps = torch.randn_like(real_data)
    t_inhom_expanded = t_inhom[:, None, :, None, None]
    noisy_data = student.noise_scheduler.forward_process(real_data, eps, t_inhom_expanded)
    print(f"  noisy_data: {noisy_data.shape}")

    # Student forward
    gen_data = student(noisy_data, t_inhom, condition=condition, fwd_pred_type="x0", is_ar=False)
    print(f"  gen_data: {gen_data.shape}")

    # L2 loss + backward
    loss = 0.5 * F.mse_loss(gen_data, real_data, reduction="mean")
    loss.backward()

    grad_count = sum(1 for p in student.parameters() if p.grad is not None)
    print(f"\n  Loss: {loss.item():.6f}")
    print(f"  Params with gradients: {grad_count}")
    print(f"  Peak memory: {gpu_peak():.1f} GB")
    print(f"  TEST 2 PASSED!")

    del student, gen_data, noisy_data, data
    gc.collect()
    torch.cuda.empty_cache()
    return True


