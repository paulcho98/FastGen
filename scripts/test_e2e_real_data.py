"""End-to-end verification of all three training paths with real data and real models.

Tests:
1. ODE KD (Stage 1a): 1.3B causal student on real ODE trajectories
2. Diffusion Forcing (Stage 1b): 1.3B causal student on real video data
3. Self-Forcing DMD (Stage 2): 14B teacher + 1.3B fake_score + 1.3B causal student

Run: CUDA_VISIBLE_DEVICES=2 python scripts/test_e2e_real_data.py
"""

import gc
import os
import sys
import time
import traceback

import torch
import torch.nn.functional as F

DEVICE = torch.device("cuda")
DTYPE = torch.bfloat16

OMNIAVATAR_ROOT = "/home/work/.local/OmniAvatar"
DATA_ROOT = "/home/work/stableavatar_data/v2v_training_data"
DATA_LIST = f"{DATA_ROOT}/video_square_path.txt"
MASK_PATH = "/home/work/.local/Self-Forcing_LipSync_StableAvatar/diffsynth/utils/mask.png"
TEACHER_CKPT = "/home/work/output_omniavatar_v2v_maskall_refseq_new_data_loss_weights_mouth_weights/step-1500.pt"
BASE_14B = ",".join(
    f"{OMNIAVATAR_ROOT}/pretrained_models/Wan2.1-T2V-14B/diffusion_pytorch_model-0000{i}-of-00006.safetensors"
    for i in range(1, 7)
)
BASE_1_3B = f"{OMNIAVATAR_ROOT}/pretrained_models/Wan2.1-T2V-1.3B/diffusion_pytorch_model.safetensors"


def gpu_mem():
    return torch.cuda.memory_allocated(DEVICE) / 1024**3


def gpu_peak():
    return torch.cuda.max_memory_allocated(DEVICE) / 1024**3


def print_header(msg):
    print(f"\n{'='*70}")
    print(f"  {msg}")
    print(f"{'='*70}")


def load_real_sample(need_path=False):
    """Load one real sample from the dataset.

    When need_path=True, looks for samples with ode_path.pt (our script's output).
    """
    from fastgen.datasets.omniavatar_dataloader import OmniAvatarDataset

    ds = OmniAvatarDataset(
        data_list_path=DATA_LIST,
        latentsync_mask_path=MASK_PATH,
        use_ref_sequence=True,
        load_ode_path=need_path,
    )

    # Find a sample (with ode_path.pt if needed)
    for i in range(min(len(ds), 100)):
        sample = ds[i]
        if sample is None:
            continue
        if need_path and "path" not in sample:
            continue
        # Move to device
        batch = {}
        for k, v in sample.items():
            if isinstance(v, torch.Tensor):
                batch[k] = v.unsqueeze(0).to(DEVICE)
            else:
                batch[k] = v
        sample_name = ds.dirs[i].split("/")[-1]
        print(f"  Loaded sample {i}: {sample_name}")
        for k, v in batch.items():
            if isinstance(v, torch.Tensor):
                print(f"    {k}: {v.shape} {v.dtype}")
        if need_path:
            # Verify it's from our script (ode_path.pt, not path.pth)
            import os
            ode_file = os.path.join(ds.dirs[i], "ode_path.pt")
            if os.path.exists(ode_file):
                print(f"    Source: ode_path.pt (our generation script)")
            else:
                print(f"    Source: path.pth (reference repo — fallback)")
        return batch

    raise RuntimeError("Could not find suitable sample" + (" with ODE path" if need_path else ""))


def build_condition(data):
    """Build OmniAvatar condition dict from data batch."""
    real = data["real"]
    ref_latent = real[:, :, :1, :, :]
    mask = data["mask"]
    if mask.dim() == 3:
        mask = mask[0]

    condition = {
        "text_embeds": data["text_embeds"].squeeze(1) if data["text_embeds"].dim() == 4 else data["text_embeds"],
        "audio_emb": data["audio_emb"],
        "ref_latent": ref_latent,
        "mask": mask,
        "masked_video": data["masked_video"],
    }
    if "ref_sequence" in data:
        condition["ref_sequence"] = data["ref_sequence"]
    return condition


