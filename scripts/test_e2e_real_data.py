# TEST 3: Self-Forcing DMD with real teacher/student/fake_score
# ============================================================
def test_self_forcing_dmd():
    print_header("TEST 3: Self-Forcing DMD — 14B teacher + 1.3B fake_score + 1.3B student")
    torch.cuda.reset_peak_memory_stats(DEVICE)

    from fastgen.networks.OmniAvatar.network import OmniAvatarWan
    from fastgen.networks.OmniAvatar.network_causal import CausalOmniAvatarWan

    # Load real sample
    data = load_real_sample(need_path=False)
    condition = build_condition(data)
    neg_condition = {
        "text_embeds": data["neg_text_embeds"].squeeze(1) if data["neg_text_embeds"].dim() == 4 else data["neg_text_embeds"],
        "audio_emb": torch.zeros_like(data["audio_emb"]),
        "ref_latent": condition["ref_latent"],
        "mask": condition["mask"],
        "masked_video": condition["masked_video"],
    }
    if "ref_sequence" in condition:
        neg_condition["ref_sequence"] = condition["ref_sequence"]

    real_data = data["real"]

    # --- Load 14B teacher ---
    print("\n  Loading 14B teacher (bidirectional)...")
    t0 = time.time()
    teacher = OmniAvatarWan(
        model_size="14B",
        in_dim=65,
        mode="v2v",
        use_audio=True,
        audio_hidden_size=32,
        base_model_paths=BASE_14B,
        omniavatar_ckpt_path=TEACHER_CKPT,
        merge_lora=True,
        net_pred_type="flow",
        schedule_type="rf",
    )
    teacher = teacher.to(device=DEVICE, dtype=DTYPE)
    teacher.eval().requires_grad_(False)
    print(f"  Teacher loaded: {gpu_mem():.1f} GB ({time.time()-t0:.0f}s)")

    # --- Load 1.3B fake_score ---
    print("  Loading 1.3B fake_score (bidirectional)...")
    fake_score = OmniAvatarWan(
        model_size="1.3B",
        in_dim=65,
        mode="v2v",
        use_audio=True,
        audio_hidden_size=32,
        base_model_paths=BASE_1_3B,
        omniavatar_ckpt_path=None,
        net_pred_type="flow",
        schedule_type="rf",
    )
    fake_score = fake_score.to(device=DEVICE, dtype=DTYPE)
    fake_score.eval().requires_grad_(False)
    print(f"  Fake score loaded: {gpu_mem():.1f} GB")

    # --- Load 1.3B causal student ---
    print("  Loading 1.3B causal student...")
    student = CausalOmniAvatarWan(
        model_size="1.3B",
        in_dim=65,
        mode="v2v",
        use_audio=True,
        audio_hidden_size=32,
        chunk_size=3,
        total_num_frames=21,
        base_model_paths=BASE_1_3B,
        omniavatar_ckpt_path=None,
        net_pred_type="flow",
        schedule_type="rf",
    )
    student = student.to(device=DEVICE, dtype=DTYPE)
    student.train()
    print(f"  Student loaded: {gpu_mem():.1f} GB")
    print(f"  All 3 models in memory: {gpu_mem():.1f} GB")

    # --- Self-Forcing rollout (all 7 chunks, gradient on last 2) ---
    num_frames = real_data.shape[2]
    print(f"\n  Running Self-Forcing rollout (all {num_frames // student.chunk_size} chunks, gradient on last 2)...")
    student.clear_caches()

    B = real_data.shape[0]
    chunk_size = student.chunk_size
    num_blocks = num_frames // chunk_size
    noise = torch.randn_like(real_data)
    noise = student.noise_scheduler.latents(noise=noise)  # scale by sigma_max

    t_list = torch.tensor([0.999, 0.937, 0.833, 0.624, 0.0], device=DEVICE, dtype=torch.float64)
    denoised_blocks = []

    for block_idx in range(num_blocks):
        cur_start_frame = 0 if block_idx == 0 else chunk_size * block_idx
        cur_end_frame = chunk_size * (block_idx + 1)
        noisy_input = noise[:, :, cur_start_frame:cur_end_frame]

        # Denoise: exit at random step
        exit_step = torch.randint(0, 4, (1,)).item()
        for step in range(len(t_list) - 1):
            t_cur = t_list[step].expand(B)
            if step == exit_step:
                # Exit step: with gradient on last 2 chunks only
                enable_grad = block_idx >= num_blocks - 2
                with torch.set_grad_enabled(enable_grad):
                    x0_pred = student(
                        noisy_input, t_cur, condition=condition,
                        fwd_pred_type="x0", cur_start_frame=cur_start_frame,
                        store_kv=False, is_ar=True,
                    )
                break
            else:
                with torch.no_grad():
                    x0_pred = student(
                        noisy_input, t_cur, condition=condition,
                        fwd_pred_type="x0", cur_start_frame=cur_start_frame,
                        store_kv=False, is_ar=True,
                    )
                t_next = t_list[step + 1].expand(B)
                eps_infer = torch.randn_like(x0_pred)
                noisy_input = student.noise_scheduler.forward_process(x0_pred, eps_infer, t_next)

        denoised_blocks.append(x0_pred)

        # Cache update (no grad)
        with torch.no_grad():
            t_zero = torch.zeros(B, device=DEVICE, dtype=DTYPE)
            _ = student(
                x0_pred, t_zero, condition=condition,
                fwd_pred_type="x0", cur_start_frame=cur_start_frame,
                store_kv=True, is_ar=True,
            )

    gen_data = torch.cat(denoised_blocks, dim=2)
    print(f"  Rollout output: {gen_data.shape}")
    print(f"  Memory after rollout: {gpu_mem():.1f} GB")

    # --- VSD loss with teacher ---
    print("  Computing VSD loss with teacher...")
    t_vsd = student.noise_scheduler.sample_t(
        B, time_dist_type="shifted", min_t=0.001, max_t=0.999, device=DEVICE
    )
    eps_vsd = torch.randn_like(gen_data)
    perturbed = student.noise_scheduler.forward_process(gen_data, eps_vsd, t_vsd)

    with torch.no_grad():
        # Teacher prediction (on gen_data subset matching rollout)
        teacher_x0 = teacher(perturbed, t_vsd, condition=condition, fwd_pred_type="x0")
        print(f"  Teacher x0: {teacher_x0.shape}")

        # Fake score prediction
        fake_x0 = fake_score(perturbed, t_vsd, condition=condition, fwd_pred_type="x0")
        print(f"  Fake score x0: {fake_x0.shape}")

    # VSD loss
    w = 1.0 / (torch.abs(gen_data - teacher_x0.detach()) + 1e-6)
    vsd_grad = (fake_x0.detach() - teacher_x0.detach()) * w
    pseudo_target = gen_data - vsd_grad
    vsd_loss = 0.5 * F.mse_loss(gen_data, pseudo_target.detach(), reduction="mean")

    print(f"\n  VSD loss: {vsd_loss.item():.6f}")

    # Backward
    vsd_loss.backward()
    grad_count = sum(1 for p in student.parameters() if p.grad is not None)
    print(f"  Student params with gradients: {grad_count}")

    # --- Fake score DSM update (separate step — free student graph first) ---
    print("\n  Testing fake score DSM update...")
    # In real training, this is a separate iteration (student_update_freq).
    # Free everything from the student update to reclaim memory.
    gen_data_for_dsm = gen_data.detach().clone()
    student.zero_grad(set_to_none=True)
    del vsd_loss, pseudo_target, vsd_grad, w, gen_data, denoised_blocks, noise
    del teacher_x0, fake_x0, perturbed, eps_vsd
    try:
        del teacher_x0_cfg, teacher_x0_neg
    except NameError:
        pass
    # Free teacher — not needed for DSM loss (only fake_score + gen_data)
    del teacher
    eps_vsd = torch.randn(1, 16, 21, 64, 64, device=DEVICE, dtype=DTYPE)  # fresh noise for DSM
    gc.collect()
    torch.cuda.empty_cache()
    print(f"  Memory after cleanup: {gpu_mem():.1f} GB")

    fake_score.train().requires_grad_(True)
    student.eval().requires_grad_(False)

    with torch.no_grad():
        x_t_sg = student.noise_scheduler.forward_process(gen_data_for_dsm, eps_vsd, t_vsd)

    fake_pred = fake_score(x_t_sg, t_vsd, condition=condition, fwd_pred_type="x0")
    dsm_loss = F.mse_loss(fake_pred, gen_data_for_dsm, reduction="mean")
    dsm_loss.backward()

    fs_grad_count = sum(1 for p in fake_score.parameters() if p.grad is not None)
    print(f"  DSM loss: {dsm_loss.item():.6f}")
    print(f"  Fake score params with gradients: {fs_grad_count}")

    # --- CFG with neg_condition ---
    print("\n  Testing CFG with negative condition...")
    with torch.no_grad():
        teacher_x0_neg = teacher(perturbed, t_vsd, condition=neg_condition, fwd_pred_type="x0")
    teacher_x0_cfg = teacher_x0 + 4.5 * (teacher_x0 - teacher_x0_neg)
    print(f"  CFG teacher x0: {teacher_x0_cfg.shape}, range [{teacher_x0_cfg.min():.3f}, {teacher_x0_cfg.max():.3f}]")

    print(f"\n  Peak memory: {gpu_peak():.1f} GB")
    print(f"  TEST 3 PASSED!")

    student.clear_caches()
    del teacher, fake_score, student, gen_data
    gc.collect()
    torch.cuda.empty_cache()
    return True


